// ─── Main page ────────────────────────────────────────────────────────────────

export default function ReadingPage() {
  const navigate = useNavigate();

  const [natal,       setNatal]       = useState(null);
  const [aspects,     setAspects]     = useState([]);
  const [focus,       setFocus]       = useState('general');
  const [mode,        setMode]        = useState('transit');
  const [status,      setStatus]      = useState('idle'); // idle | loading | done | error
  const [reading,     setReading]     = useState(null);
  const [errorMsg,    setErrorMsg]    = useState('');
  const [configured,  setConfigured]  = useState(true);
  const [pastReadings,setPastReadings]= useState([]);
  const [deepDive,    setDeepDive]    = useState(null); // { planet, text, loading }
  const [puristMode,  setPuristMode]  = useState(false);
  const [userQuery,   setUserQuery]   = useState('');
  const [confirmModal,setConfirmModal]= useState(null);
  const abortRef = useRef(false);

  // Load data on mount
  useEffect(() => {
    const settings = store.getJSON('ephi_settings') || {};
    setPuristMode(settings.puristMode || false);

    setConfigured(isGeminiConfigured());
    const natalData = getNatalFromStorage();
    setNatal(natalData);

    const aspectData = getAspectsFromStorage();
    if (aspectData?.aspects) {
      setAspects(aspectData.aspects);
    }

    const loadPast = async () => {
      const past = await getRecentReadings();
      setPastReadings(past);
    };
    loadPast();
  }, []);

  // Fetch fresh aspects if not cached
  useEffect(() => {
    if (!natal || aspects.length > 0) return;

    function calcAspects() {
      try {
        const now = new Date();
        const transitPositions = getPlanetPositions(now);
        const natalPositions = natal.positions;
        
        const tn = getActiveAspects(transitPositions, natalPositions);
        setAspects(tn || []);
        
        // Cache for this session
        store.setJSON('astro_aspects', { aspects: tn, timestamp: now.toISOString() });
      } catch (err) {
        console.error('Failed to calculate aspects locally:', err);
      }
    }

    calcAspects();
  }, [natal]);

  async function handleGenerate() {
    if (!natal) return;

    const existingFresh = await getLatestReading(focus);
    if (existingFresh) {
      const focusLabel = FOCUS_AREAS.find(f => f.value === focus)?.label || focus;
      setConfirmModal({
        focusLabel,
        timeAgoStr: timeAgo(existingFresh.timestamp),
        existingFresh
      });
      return;
    }

    executeGeneration();
  }

  async function executeGeneration() {
    abortRef.current = false;
    setStatus('loading');
    setErrorMsg('');
    setReading(null);

    try {
      const result = await generateReading({ 
        natal, 
        aspects, 
        focus, 
        userQuery: focus === 'custom' ? userQuery : '',
        mode
      });
      if (abortRef.current) return;

      const saved = await saveReading({
        focus,
        text: result.text,
        aspects,
        aspectCount: aspects.length,
      });

      const past = await getRecentReadings();
      setPastReadings(past);
      setReading(saved);
      setStatus('done');
    } catch (err) {
      if (abortRef.current) return;
      setErrorMsg(err.message || 'Failed to generate reading.');
      setStatus('error');
    }
  }

  function handleNewReading() {
    setReading(null);
    setStatus('idle');
    setErrorMsg('');
  }

  function handleSelectPast(r) {
    setReading(r);
    setStatus('done');
  }

  async function handlePlanetClick(planet, set) {
    setDeepDive({ planet, loading: true, text: '' });
    try {
      const res = await generateAspectReading({
        transitPlanet: planet,
        natalPlanet: planet, // Simple self-reflection for hotspots
        aspectName: 'conjunction',
        natal
      });
      setDeepDive({ planet, loading: false, text: res.text });
    } catch (err) {
      setDeepDive({ planet, loading: false, text: 'The Oracle is silent on this planet today.' });
    }
  }

  async function handleAspectClick(asp) {
    const transitP = asp.transitPlanet || asp.planet1;
    const natalP = asp.natalPlanet || asp.planet2;
    setDeepDive({ planet: `${transitP} ${asp.aspectName} ${natalP}`, loading: true, text: '' });
    try {
      const res = await generateAspectReading({
        transitPlanet: transitP,
        natalPlanet: natalP,
        aspectName: asp.aspectName,
        natal
      });
      setDeepDive({ planet: `${transitP} ${asp.aspectName} ${natalP}`, loading: false, text: res.text });
    } catch (err) {
      setDeepDive({ planet: 'Aspect Deep-Dive', loading: false, text: 'The Oracle is silent on this connection.' });
    }
  }

  // ── Render guards ──────────────────────────────────────────────────────────
  if (!configured) return <NoKeyBanner />;
  if (natal === null && status === 'idle') return (
    <div className="page-wrap">
      <div className="reading-loading-wrap">
        <div style={{ color: 'var(--text-muted)', fontSize: '0.9rem' }}>Loading…</div>
      </div>
    </div>
  );
  if (!natal) return <NoNatalBanner />;

  // ── Done — show reading ────────────────────────────────────────────────────
  if (status === 'done' && reading) {
    return (
      <div className="page-wrap">
        <ReadingDisplay reading={reading} onNew={handleNewReading} />
        <PastReadings readings={pastReadings.slice(1)} onSelect={handleSelectPast} />
      </div>
    );
  }

  // ── Loading ────────────────────────────────────────────────────────────────
  if (status === 'loading') {
    return (
      <div className="page-wrap">
        <LoadingState focus={focus} />
      </div>
    );
  }

  // ── Idle / error — main UI ─────────────────────────────────────────────────
  return (
    <div className="page-wrap">
      {/* Page header */}
      <div className="page-header">
        <span className="page-label">The Oracle</span>
        <h1 className="page-title">
          <UiIcon name="sparkle" size={28} color="var(--accent)" style={{ marginRight: '12px' }} />
          AI Reading
        </h1>
        <p className="page-subtitle">
          Gemini synthesizes your natal chart with today's live transits into a personalised reading.
        </p>
      </div>

      <AdSlot type="banner" slotId="reading-top" />

      {/* Interactive Chart Wheel */}
      <div className="chart-container" style={{ marginBottom: '3rem' }}>
        <ChartWheel 
          planets={natal?.positions || {}} 
          ascendant={natal?.asc || 0}
          size={Math.min(window.innerWidth - 40, 500)}
          bgColor="var(--bg-card)"
          strokeColor="var(--border)"
          onPlanetClick={handlePlanetClick}
          onAspectClick={handleAspectClick}
        />
        <p style={{ textAlign: 'center', fontSize: '0.75rem', color: 'var(--text-muted)', marginTop: '1rem' }}>
          ✦ Click any planet for a specialized deep-dive reading.
        </p>
      </div>

      {/* Natal summary */}
      <div className="reading-natal-summary card" style={{ textAlign: 'center' }}>
        <div className="reading-natal-label">Reading for</div>
        <div className="reading-natal-value">
          {natal.meta?.birthDate || 'your natal chart'}
          {natal.meta?.birthCity ? ` · ${natal.meta.birthCity}` : ''}
        </div>
      </div>

      {/* Active aspects count */}
      {aspects.length > 0 && (
        <div className="reading-aspects-summary">
          <span className="reading-aspects-dot" />
          <span className="reading-aspects-text">
            {aspects.filter(a => ['exact','strong'].includes(a.strength)).length} significant transits active now
          </span>
        </div>
      )}

      {/* Reading Type selector */}
      <div className="reading-section">
        <div className="reading-section-title">Reading Type</div>
        <div style={{ display: 'flex', gap: '0.5rem', background: 'var(--bg-card)', padding: '4px', borderRadius: 'var(--radius-md)', border: '1px solid var(--border-color)' }}>
          <button 
            className={`btn ${mode === 'transit' ? 'btn-primary' : 'btn-ghost'}`}
            style={{ flex: 1, padding: '0.75rem', borderRadius: '4px', fontSize: '0.9rem' }}
            onClick={() => setMode('transit')}
            disabled={status === 'loading'}
          >
            Transit Forecast
          </button>
          <button 
            className={`btn ${mode === 'natal' ? 'btn-primary' : 'btn-ghost'}`}
            style={{ flex: 1, padding: '0.75rem', borderRadius: '4px', fontSize: '0.9rem' }}
            onClick={() => setMode('natal')}
            disabled={status === 'loading'}
          >
            Natal Blueprint
          </button>
        </div>
        <p style={{ fontSize: '0.8rem', color: 'var(--text-muted)', marginTop: '0.75rem', textAlign: 'center' }}>
          {mode === 'transit' ? "Analyzes how today's active transits are triggering your chart." : 'A timeless deep dive into your fundamental soul architecture.'}
        </p>
      </div>

      {/* Focus selector */}
      <div className="reading-section">
        <div className="reading-section-title">Choose your focus</div>
        <FocusSelector
          selected={focus}
          onChange={setFocus}
          disabled={status === 'loading'}
        />
      </div>

      {/* Custom Inquiry Box */}
      {focus === 'custom' && (
        <div className="reading-section">
          <div className="reading-section-title">Your Question</div>
          <textarea
            className="reading-inquiry-box"
            placeholder="e.g. 'What does my chart say about my hidden talents?' or 'Will I move abroad this year?'"
            value={userQuery}
            onChange={(e) => setUserQuery(e.target.value)}
            disabled={status === 'loading'}
          />
          <p style={{ fontSize: '0.75rem', color: 'var(--text-muted)', marginTop: '0.5rem' }}>
            Be specific! Mention the life area or transition you're curious about.
          </p>
        </div>
      )}

      {/* Error */}
      {status === 'error' && (
        <div className="reading-error-box">
          <UiIcon name="warning" size={16} />
          <span>{errorMsg}</span>
        </div>
      )}

      {/* Generate button */}
      <div className="reading-generate-wrap">
        <button className="btn btn-primary" onClick={handleGenerate} style={{ width: '100%', padding: '1rem' }}>
          <UiIcon name="sparkle" size={18} />
          Generate {FOCUS_AREAS.find(f => f.value === focus)?.label} Reading
        </button>
        <p className="reading-generate-note">
          Uses ~1 of your 15 free Gemini requests per minute
        </p>
      </div>

      {/* Past readings */}
      <PastReadings readings={pastReadings} onSelect={handleSelectPast} />

      {/* Confirm Modal */}
      {confirmModal && (
        <div className="reading-modal-overlay">
          <div className="reading-modal-content card">
            <div className="reading-modal-icon"><UiIcon name="sparkle" size={32} /></div>
            <h3 className="card-title">Existing Reading Found</h3>
            <p className="reading-modal-text">
              You already have a <strong>{confirmModal.focusLabel}</strong> reading generated {confirmModal.timeAgoStr}. Would you like to generate a new one or view the existing one?
            </p>
            <div className="reading-modal-actions">
              <button
                className="btn btn-ghost"
                onClick={() => {
                  setReading(confirmModal.existingFresh);
                  setStatus('done');
                  setConfirmModal(null);
                }}
              >
                View Existing
              </button>
              <button
                className="btn btn-primary"
                onClick={() => {
                  setConfirmModal(null);
                  executeGeneration();
                }}
              >
                Generate New
              </button>
            </div>
          </div>
        </div>
      )}

      {/* Deep Dive Modal */}
      {deepDive && (
        <div className="reading-modal-overlay" onClick={() => setDeepDive(null)}>
          <div className="reading-modal-content card" style={{ maxWidth: '600px', maxHeight: '80vh', overflowY: 'auto' }} onClick={e => e.stopPropagation()}>
            <div style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'center', marginBottom: '1.5rem' }}>
              <div style={{ display: 'flex', alignItems: 'center', gap: '0.75rem' }}>
                <div style={{ padding: '10px', background: 'var(--bg-deep)', borderRadius: '10px', border: '1px solid var(--border)' }}>
                  <UiIcon name="sparkle" size={20} color="var(--accent)" />
                </div>
                <h3 style={{ margin: 0, fontSize: '1.2rem', textTransform: 'capitalize' }}>{deepDive.planet} Deep-Dive</h3>
              </div>
              <button onClick={() => setDeepDive(null)} style={{ background: 'none', border: 'none', color: 'var(--text-muted)', cursor: 'pointer', fontSize: '1.5rem' }}>×</button>
            </div>

            {deepDive.loading ? (
              <div style={{ padding: '3rem', textAlign: 'center' }}>
                <div className="spinner" style={{ margin: '0 auto 1rem' }} />
                <p>Consulting the archives...</p>
              </div>
            ) : (
              <div className="reading-body" style={{ padding: 0 }}>
                <EphiMarkdown text={deepDive.text} />
              </div>
            )}
          </div>
        </div>
      )}
    </div>
  );
}
