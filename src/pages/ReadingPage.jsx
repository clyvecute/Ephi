// ─── Helpers ──────────────────────────────────────────────────────────────────

function getNatalFromStorage() {
  try {
    return store.getJSON('astro_natal');
  } catch { return null; }
}

function getAspectsFromStorage() {
  try {
    return store.getJSON('astro_aspects');
  } catch { return null; }
}

function timeAgo(isoStr) {
  const diff = Date.now() - new Date(isoStr).getTime();
  const m = Math.floor(diff / 60000);
  if (m < 1)  return 'just now';
  if (m < 60) return `${m}m ago`;
  const h = Math.floor(m / 60);
  if (h < 24) return `${h}h ago`;
  return `${Math.floor(h / 24)}d ago`;
}

// ─── Sub-components ───────────────────────────────────────────────────────────

function FocusSelector({ selected, onChange, disabled }) {
  return (
    <div className="reading-focus-row">
      {FOCUS_AREAS.map((f) => (
        <button
          key={f.value}
          className={`reading-focus-btn ${selected === f.value ? 'active' : ''}`}
          onClick={() => !disabled && onChange(f.value)}
          disabled={disabled}
        >
          <span className="reading-focus-emoji">{f.emoji}</span>
          <span>{f.label}</span>
        </button>
      ))}
    </div>
  );
}

function LoadingState({ focus }) {
  const focusLabel = FOCUS_AREAS.find((f) => f.value === focus)?.label || 'General';
  const [dot, setDot] = useState(0);

  useEffect(() => {
    const t = setInterval(() => setDot((d) => (d + 1) % 4), 500);
    return () => clearInterval(t);
  }, []);

  const messages = [
    'Reading the current sky…',
    'Consulting your natal chart…',
    `Weaving your ${focusLabel} reading…`,
    'Synthesizing the transits…',
    'Almost ready…',
  ];
  const [msgIdx, setMsgIdx] = useState(0);

  useEffect(() => {
    const t = setInterval(() => setMsgIdx((i) => (i + 1) % messages.length), 2200);
    return () => clearInterval(t);
  }, []);

  return (
    <div className="reading-loading-wrap">
      <div className="reading-loading-glyph"><UiIcon name="sparkle" size={48} /></div>
      <div className="reading-loading-msg">
        {messages[msgIdx]}{'·'.repeat(dot + 1)}
      </div>
      <div className="reading-loading-bar">
        <div className="reading-loading-bar-inner" />
      </div>
      <p className="reading-loading-note">Gemini is reading your chart — usually takes 5–15 seconds</p>
    </div>
  );
}

function ReadingDisplay({ reading, onNew }) {
  const focusMeta = FOCUS_AREAS.find((f) => f.value === reading.focus);
  const toast = useToast();

  const handleShare = () => {
    const text = `✦ Ephi Oracle Reading ✦\nFocus: ${focusMeta?.label || reading.focus}\n\n${reading.text}\n\nGenerated by Ephi: The Midnight Grimoire`;
    navigator.clipboard.writeText(text);
    toast('Reading copied to clipboard.');
  };

  return (
    <div className="reading-display-wrap">
      {/* Reading meta bar */}
      <div className="reading-meta">
        <div className="reading-meta-left">
          <span className="pill">
            {focusMeta?.label || reading.focus}
          </span>
          <span className="reading-time">{timeAgo(reading.timestamp)}</span>
          {reading.aspectCount > 0 && (
            <span className="reading-aspect-count">{reading.aspectCount} active transits</span>
          )}
        </div>
        <div style={{ display: 'flex', gap: '0.5rem' }}>
          <button className="btn btn-ghost" onClick={() => window.print()} style={{ padding: '0.4rem 0.8rem', fontSize: '0.75rem' }}>
            <UiIcon name="pin" size={12} style={{ marginRight: 6 }} />
            Print to Grimoire
          </button>
          <button className="btn btn-ghost" onClick={handleShare} style={{ padding: '0.4rem 0.8rem', fontSize: '0.75rem' }}>
            <UiIcon name="sparkle" size={12} style={{ marginRight: 6 }} />
            Share
          </button>
          <button className="btn btn-ghost" onClick={onNew} style={{ padding: '0.4rem 0.8rem', fontSize: '0.75rem' }}>
            New reading
          </button>
        </div>
      </div>

      {/* Reading body */}
      <div className="reading-body card">
        <EphiMarkdown text={reading.text} />
        
        <div style={{ 
          marginTop: '2rem', 
          paddingTop: '1rem', 
          borderTop: '1px dashed var(--border)',
          textAlign: 'center'
        }}>
          <p style={{ fontSize: '0.8rem', color: 'var(--text-muted)', marginBottom: '0.5rem' }}>
            Found this reading helpful?
          </p>
          <Link 
            to="/support" 
            style={{ 
              color: 'var(--accent)', 
              fontSize: '0.75rem', 
              fontWeight: '700', 
              textDecoration: 'none',
              textTransform: 'uppercase',
              letterSpacing: '0.1em'
            }}
          >
            ✦ Support the Oracle
          </Link>
        </div>
      </div>

      {/* Footer */}
      <div className="reading-footer">
        <span>Generated by Gemini Flash · </span>
        <Link to="/dashboard" className="reading-back-link">← Back to live transits</Link>
      </div>
    </div>
  );
}

function PastReadings({ readings, onSelect }) {
  if (!readings.length) return null;
  return (
    <div className="reading-past-wrap">
      <div className="reading-past-label">Past readings</div>
      {readings.map((r, i) => {
        const focusMeta = FOCUS_AREAS.find((f) => f.value === r.focus);
        return (
          <button key={i} className="reading-past-item" onClick={() => onSelect(r)}>
            <span className="reading-past-emoji">{focusMeta?.emoji || '✦'}</span>
            <span className="reading-past-focus">{focusMeta?.label || r.focus}</span>
            <span className="reading-past-time">{timeAgo(r.timestamp)}</span>
            <span className="reading-past-chevron">›</span>
          </button>
        );
      })}
    </div>
  );
}

